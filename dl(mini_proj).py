# Model 1: LSTM with Attention
def build_lstm_attention(input_shape, dropout=0.3, l2_reg=0.001):
    """
    LSTM with self-attention mechanism
    Best for capturing long-term dependencies in time series
    """
    inputs = layers.Input(shape=input_shape)

    # LSTM layers with dropout
    lstm1 = layers.LSTM(128, return_sequences=True,
                        kernel_regularizer=regularizers.l2(l2_reg))(inputs)
    lstm1 = layers.Dropout(dropout)(lstm1)
    lstm1 = layers.LayerNormalization()(lstm1)

    lstm2 = layers.LSTM(64, return_sequences=True,
                        kernel_regularizer=regularizers.l2(l2_reg))(lstm1)
    lstm2 = layers.Dropout(dropout)(lstm2)
    lstm2 = layers.LayerNormalization()(lstm2)

    # Attention mechanism
    attention = layers.MultiHeadAttention(num_heads=4, key_dim=16)(lstm2, lstm2)
    attention = layers.Dropout(dropout)(attention)

    # Global pooling
    avg_pool = layers.GlobalAveragePooling1D()(attention)
    max_pool = layers.GlobalMaxPooling1D()(attention)
    concat = layers.Concatenate()([avg_pool, max_pool])

    # Dense layers
    dense1 = layers.Dense(64, activation='relu',
                         kernel_regularizer=regularizers.l2(l2_reg))(concat)
    dense1 = layers.Dropout(dropout)(dense1)
    dense1 = layers.LayerNormalization()(dense1)

    dense2 = layers.Dense(32, activation='relu',
                         kernel_regularizer=regularizers.l2(l2_reg))(dense1)
    dense2 = layers.Dropout(dropout)(dense2)

    # Output
    outputs = layers.Dense(1, activation='sigmoid')(dense2)

    model = keras.Model(inputs=inputs, outputs=outputs)
    return model

# Model 2: 1D CNN-LSTM Hybrid
def build_cnn_lstm(input_shape, dropout=0.3, l2_reg=0.001):
    """
    CNN for feature extraction + LSTM for temporal patterns
    Good for capturing both local and global patterns
    """
    inputs = layers.Input(shape=input_shape)

    # 1D CNN layers
    conv1 = layers.Conv1D(64, kernel_size=3, activation='relu', padding='same',
                         kernel_regularizer=regularizers.l2(l2_reg))(inputs)
    conv1 = layers.BatchNormalization()(conv1)
    conv1 = layers.Dropout(dropout)(conv1)

    conv2 = layers.Conv1D(32, kernel_size=3, activation='relu', padding='same',
                         kernel_regularizer=regularizers.l2(l2_reg))(conv1)
    conv2 = layers.BatchNormalization()(conv2)
    conv2 = layers.Dropout(dropout)(conv2)

    # LSTM layers
    lstm = layers.LSTM(64, return_sequences=False,
                      kernel_regularizer=regularizers.l2(l2_reg))(conv2)
    lstm = layers.Dropout(dropout)(lstm)

    # Dense layers
    dense = layers.Dense(32, activation='relu',
                        kernel_regularizer=regularizers.l2(l2_reg))(lstm)
    dense = layers.Dropout(dropout)(dense)

    # Output
    outputs = layers.Dense(1, activation='sigmoid')(dense)

    model = keras.Model(inputs=inputs, outputs=outputs)
    return model

# Model 3: Bidirectional LSTM (Best for small datasets)
def build_bilstm(input_shape, dropout=0.3, l2_reg=0.001):
    """
    Bidirectional LSTM - processes sequences forward and backward
    Excellent for small datasets with strong temporal patterns
    """
    inputs = layers.Input(shape=input_shape)

    # Bidirectional LSTM layers
    bilstm1 = layers.Bidirectional(
        layers.LSTM(64, return_sequences=True,
                   kernel_regularizer=regularizers.l2(l2_reg))
    )(inputs)
    bilstm1 = layers.Dropout(dropout)(bilstm1)
    bilstm1 = layers.LayerNormalization()(bilstm1)

    bilstm2 = layers.Bidirectional(
        layers.LSTM(32, return_sequences=False,
                   kernel_regularizer=regularizers.l2(l2_reg))
    )(bilstm1)
    bilstm2 = layers.Dropout(dropout)(bilstm2)

    # Dense layers
    dense1 = layers.Dense(32, activation='relu',
                         kernel_regularizer=regularizers.l2(l2_reg))(bilstm2)
    dense1 = layers.Dropout(dropout)(dense1)

    dense2 = layers.Dense(16, activation='relu',
                         kernel_regularizer=regularizers.l2(l2_reg))(dense1)
    dense2 = layers.Dropout(dropout)(dense2)

    # Output
    outputs = layers.Dense(1, activation='sigmoid')(dense2)

    model = keras.Model(inputs=inputs, outputs=outputs)
    return model

# Model 4: Transformer-based (Transfer Learning Approach)
def build_transformer(input_shape, dropout=0.3, l2_reg=0.001):
    """
    Transformer encoder for time series
    Uses self-attention across entire sequence
    """
    inputs = layers.Input(shape=input_shape)

    # Positional encoding
    positions = tf.range(start=0, limit=input_shape[0], delta=1)
    position_embedding = layers.Embedding(input_dim=input_shape[0],
                                         output_dim=input_shape[1])(positions)
    x = inputs + position_embedding

    # Transformer block 1
    attention1 = layers.MultiHeadAttention(num_heads=4, key_dim=32)(x, x)
    attention1 = layers.Dropout(dropout)(attention1)
    x1 = layers.Add()([x, attention1])
    x1 = layers.LayerNormalization()(x1)

    # Feed-forward network
    ffn = layers.Dense(128, activation='relu',
                      kernel_regularizer=regularizers.l2(l2_reg))(x1)
    ffn = layers.Dropout(dropout)(ffn)
    ffn = layers.Dense(input_shape[1],
                      kernel_regularizer=regularizers.l2(l2_reg))(ffn)
    x2 = layers.Add()([x1, ffn])
    x2 = layers.LayerNormalization()(x2)

    # Global pooling
    pooled = layers.GlobalAveragePooling1D()(x2)

    # Dense layers
    dense = layers.Dense(64, activation='relu',
                        kernel_regularizer=regularizers.l2(l2_reg))(pooled)
    dense = layers.Dropout(dropout)(dense)

    # Output
    outputs = layers.Dense(1, activation='sigmoid')(dense)

    model = keras.Model(inputs=inputs, outputs=outputs)
    return model

