def build_dl_features(df, lookback=12):
    """
    Build comprehensive features optimized for deep learning
    Includes temporal sequences for LSTM/1D-CNN
    """
    df = df.copy()

    # Temporal features
    df['year'] = df.index.year
    df['month'] = df.index.month
    df['week'] = df.index.isocalendar().week.astype(int)
    df['day_of_year'] = df.index.dayofyear

    # Cyclical encoding (critical for seasonality)
    df['sin_week'] = np.sin(2 * np.pi * df['week'] / 52)
    df['cos_week'] = np.cos(2 * np.pi * df['week'] / 52)
    df['sin_month'] = np.sin(2 * np.pi * df['month'] / 12)
    df['cos_month'] = np.cos(2 * np.pi * df['month'] / 12)

    # Case lag features (essential!)
    for lag in range(1, lookback + 1):
        df[f'cases_lag_{lag}'] = df['total_cases'].shift(lag)

    # Rolling statistics on cases
    for window in [3, 5, 7, 14]:
        df[f'cases_roll_mean_{window}'] = df['total_cases'].rolling(window).mean()
        df[f'cases_roll_std_{window}'] = df['total_cases'].rolling(window).std()
        df[f'cases_roll_max_{window}'] = df['total_cases'].rolling(window).max()

    # Exponential moving averages
    df['cases_ema_3'] = df['total_cases'].ewm(span=3).mean()
    df['cases_ema_7'] = df['total_cases'].ewm(span=7).mean()

    # Case momentum
    df['cases_pct_change'] = df['total_cases'].pct_change()
    df['cases_diff'] = df['total_cases'].diff()

    # Climate features
    key_climate = [
        'station_avg_temp_c',
        'station_precip_mm',
        'reanalysis_avg_temp_k',
        'reanalysis_precip_amt_kg_per_m2',
        'reanalysis_specific_humidity_g_per_kg',
        'reanalysis_relative_humidity_percent',
        'reanalysis_dew_point_temp_k'
    ]

    for col in key_climate:
        if col in df.columns:
            # Climate lags
            for lag in [1, 2, 3, 4]:
                df[f'{col}_lag_{lag}'] = df[col].shift(lag)

            # Rolling climate
            df[f'{col}_roll_mean_4'] = df[col].rolling(4).mean()
            df[f'{col}_diff'] = df[col].diff()

    # Temperature-precipitation interaction
    if 'station_avg_temp_c' in df.columns and 'station_precip_mm' in df.columns:
        df['temp_precip_interaction'] = df['station_avg_temp_c'] * df['station_precip_mm']
        df['temp_optimal'] = ((df['station_avg_temp_c'] - 27.5) ** 2) * -1

    # NDVI features
    if 'ndvi_ne' in df.columns:
        df['ndvi_avg'] = df[['ndvi_ne', 'ndvi_nw', 'ndvi_se', 'ndvi_sw']].mean(axis=1)
        df['ndvi_avg_lag_1'] = df['ndvi_avg'].shift(1)

    return df

