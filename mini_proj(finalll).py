def build_beta_features(df, lags=(1, 2, 3), roll_windows=(3, 5)):
    """Build features to predict next week's β"""
    df = df.copy()

    # Lag features for β
    for lag in lags:
        df[f'beta_lag_{lag}'] = df['beta_smooth'].shift(lag)

    # Compartment lags
    for lag in [1, 2]:
        df[f'I_lag_{lag}'] = df['I'].shift(lag)

    # Rolling stats for I
    for w in roll_windows:
        df[f'I_roll_mean_{w}'] = df['I'].rolling(w).mean()

    # Climate features
    climate_cols = ['station_avg_temp_c', 'station_precip_mm',
                    'reanalysis_specific_humidity_g_per_kg']

    for col in climate_cols:
        if col in df.columns:
            df[f'{col}_lag1'] = df[col].shift(1)
            df[f'{col}_roll3'] = df[col].rolling(3).mean()

    # Seasonality
    week = df.index.isocalendar().week.astype(int)
    df['sin_week'] = np.sin(2 * np.pi * week / 52)
    df['cos_week'] = np.cos(2 * np.pi * week / 52)

    # Target
    df['beta_target'] = df['beta_smooth'].shift(-1)

    df = df.dropna()

    return df

