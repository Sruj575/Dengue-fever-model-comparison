def iterative_forecast_beta(df_ml, X, beta_model, horizon=52):
    """Predict β iteratively with constraints"""
    df_work = df_ml.copy()
    X_curr = X.copy()

    beta_predictions = []

    for h in range(horizon):
        X_last = X_curr.iloc[[-1]]
        beta_next = beta_model.predict(X_last)[0]

        # CRITICAL: Constrain predicted beta
        beta_next = np.clip(beta_next, 0.1, 0.85)

        # Smooth with previous prediction
        if len(beta_predictions) > 0:
            beta_next = 0.8 * beta_next + 0.2 * beta_predictions[-1]

        beta_predictions.append(beta_next)

        new_row = df_work.iloc[-1:].copy()
        new_row['beta_smooth'] = beta_next

        df_work = pd.concat([df_work, new_row])
        df_work = build_beta_features(df_work)

        if len(df_work) > 0:
            X_curr = df_work[BETA_FEATURE_COLS]

    return np.array(beta_predictions)

