def seirs_derivatives(state, beta, alpha, gamma, delta):
    """SEIRS differential equations"""
    S, E, I, R = state

    dS = -beta * S * I + delta * R
    dE = beta * S * I - alpha * E
    dI = alpha * E - gamma * I
    dR = gamma * I - delta * R

    return np.array([dS, dE, dI, dR])

def rk4_step_seirs(state, beta, alpha, gamma, delta, dt=1.0):
    """RK4 integration with stability checks"""
    k1 = seirs_derivatives(state, beta, alpha, gamma, delta)
    k2 = seirs_derivatives(state + dt*k1/2, beta, alpha, gamma, delta)
    k3 = seirs_derivatives(state + dt*k2/2, beta, alpha, gamma, delta)
    k4 = seirs_derivatives(state + dt*k3, beta, alpha, gamma, delta)

    next_state = state + dt * (k1 + 2*k2 + 2*k3 + k4) / 6

    # Enforce bounds
    next_state = np.maximum(next_state, 0)
    next_state[2] = min(next_state[2], 0.02)  # Cap I at 2%
    next_state = next_state / (next_state.sum() + 1e-10)

    return next_state

def simulate_seirs_forecast(initial_state, beta_sequence, alpha, gamma, delta):
    """Simulate SEIRS with stability controls"""
    state = np.array(initial_state)

    S_list, E_list, I_list, R_list = [], [], [], []

    for i, beta in enumerate(beta_sequence):
        # Dampen beta if I is getting too large
        if state[2] > 0.015:
            beta = beta * 0.7

        # Additional: use average of predicted and historical beta for stability
        if i > 0:
            beta = 0.7 * beta + 0.3 * beta_sequence[max(0, i-1)]

        state = rk4_step_seirs(state, beta, alpha, gamma, delta)

        S_list.append(state[0])
        E_list.append(state[1])
        I_list.append(state[2])
        R_list.append(state[3])

    return np.array(S_list), np.array(E_list), np.array(I_list), np.array(R_list)

