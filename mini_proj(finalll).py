# Reindex to continuous weeks
def reindex_weekly(df):
    df = df.copy()
    df = df[~df.index.duplicated(keep="first")]
    full_range = pd.date_range(start=df.index.min(), end=df.index.max(), freq="W-MON")
    df = df.reindex(full_range)
    return df

