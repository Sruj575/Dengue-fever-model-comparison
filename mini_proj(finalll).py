def construct_SEIRS_compartments(df,
                                  N=400000,
                                  incubation_period=5,
                                  infectious_period=7,
                                  immunity_period=180):
    """
    SEIRS Model for Dengue with STABLE initialization
    """
    df = df.copy()

    # Convert parameters to weekly rates
    alpha = 7 / incubation_period
    gamma = 7 / infectious_period
    delta = 7 / immunity_period

    df['alpha'] = alpha
    df['gamma'] = gamma
    df['delta'] = delta
    df['N'] = N

    # CRITICAL FIX: Use much smaller scaling to prevent instability
    # Peak observed cases should map to ~0.1% of population, not 0.5%
    max_cases = df['cases_smooth'].max()

    # More conservative scaling: peak cases = 0.1% of population in model
    scale_to_population = 0.001

    df['I'] = (df['cases_smooth'] / max_cases) * scale_to_population
    df['I'] = df['I'].clip(lower=1e-6, upper=0.005)  # Cap at 0.5%

    # E should be slightly larger than I (incubation backlog)
    df['E'] = df['I'] * 1.5
    df['E'] = df['E'].clip(upper=0.01)

    # Estimate R from cumulative infections with decay
    R = np.zeros(len(df))
    for t in range(1, len(df)):
        new_R = gamma * df['I'].iloc[t-1]
        R[t] = R[t-1] + new_R - delta * R[t-1]
        R[t] = max(0, min(0.2, R[t]))

    df['R'] = R

    # S is remainder
    df['S'] = 1 - df['E'] - df['I'] - df['R']
    df['S'] = df['S'].clip(lower=0.7, upper=1.0)

    # Normalize
    total = df['S'] + df['E'] + df['I'] + df['R']
    df['S'] = df['S'] / total
    df['E'] = df['E'] / total
    df['I'] = df['I'] / total
    df['R'] = df['R'] / total

    # Store for reverse conversion
    df['_max_cases'] = max_cases
    df['_scale_factor'] = scale_to_population

    return df

