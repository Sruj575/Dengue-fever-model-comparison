def estimate_climate_driven_beta(df, base_beta_bounds=(0.05, 1.2)):
    """Estimate transmission rate with strict bounds"""
    df = df.copy()

    S = df['S']
    E = df['E']
    I = df['I']
    alpha = df['alpha'].iloc[0]

    E_next = df['E'].shift(-1)

    dE = E_next - E
    numerator = dE + alpha * E
    denominator = S * I

    beta_raw = numerator / (denominator + 1e-8)
    beta_raw = beta_raw.replace([np.inf, -np.inf], np.nan)

    # STRICT clipping for stability
    beta_raw = beta_raw.clip(lower=base_beta_bounds[0], upper=base_beta_bounds[1])

    # Aggressive smoothing
    beta_smooth = beta_raw.rolling(window=7, center=True, min_periods=1).mean()
    beta_smooth = beta_smooth.interpolate(method='linear').ffill().bfill()

    # Additional constraint: beta shouldn't vary too wildly
    beta_smooth = beta_smooth.clip(lower=0.1, upper=0.9)

    df['beta_raw'] = beta_raw
    df['beta_smooth'] = beta_smooth

    return df

